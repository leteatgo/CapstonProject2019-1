    private void shop2_next_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_shop2_next_buttonActionPerformed
        // TODO add your handling code here:
        String break_time = shop2_Break_Startbox.getSelectedItem().toString();
        break_time = break_time + " ~ " + shop2_Break_Endbox.getSelectedItem();
       
        rest.setBreak_time(break_time);
        
        if (shop2_hours_Box.getSelectedIndex() == 0) {
            if (!"".equals(rest.getHoliday_num())) {
                if (rest.getBreak_num() == 1) {
                    rest.setH_type(9);
                    
                } else {
                    rest.setH_type(10);
                    
                }
            } else {
                if (rest.getBreak_num() == 1) {
                    rest.setH_type(11);
                    
                } else {
                    rest.setH_type(12);
                    rest.setHours("t"+rest.getH_type());
                }
            }
            CardLayout card = (CardLayout) mainPanel.getLayout();
            card.show(mainPanel, "card5");
        }//모든시간 상이
        else if (shop2_hours_Box.getSelectedIndex() == 1) {
            if (!"".equals(rest.getHoliday_num())) {
                if (rest.getBreak_num() == 1) {
                    rest.setH_type(1);
                } else {
                    rest.setH_type(2);
                }
            } else {
                if (rest.getBreak_num() == 1) {
                    rest.setH_type(3);
                } else {
                    rest.setH_type(4);
                }
            }
            shop4_Weekday_Start.setEnabled(false);
            shop4_Weekday_End.setEnabled(false);
            shop4_Weekend_Start.setEnabled(false);
            shop4_Weekend_End.setEnabled(false);
            CardLayout card = (CardLayout) mainPanel.getLayout();
            card.show(mainPanel, "card6");
        }//모든시간동일
        else {
            if (!"".equals(rest.getHoliday_num())) {
                if (rest.getBreak_num() == 1) {
                    rest.setH_type(5);
                } else {
                    rest.setH_type(6);
                }
            } else {
                if (rest.getBreak_num() == 1) {
                    rest.setH_type(7);
                } else {
                    rest.setH_type(8);
                }
            }
            shop4_All_Start.setEnabled(false);
            shop4_All_End.setEnabled(false);
            CardLayout card = (CardLayout) mainPanel.getLayout();
            card.show(mainPanel, "card6");
        }//평일/주말

        if (rest.getHoliday_num().length() == 0) {
            if(rest.getBreak_num()==1)
                rest.setHours("\"t\":" + rest.getH_type() + rest.getBreak_time());
            else
                 rest.setHours("\"t\":" + rest.getH_type() );
        } else {
            if(rest.getBreak_num()==1)
                rest.setHours("\"t\":" + rest.getH_type() + ",\"cd\":" +rest.getHoliday_num() + rest.getBreak_time());
            else
                rest.setHours("\"t\":" + rest.getH_type() +",\"cd\":" + rest.getHoliday_num());
            
        }
        
    }//GEN-LAST:event_shop2_next_buttonActionPerformed

