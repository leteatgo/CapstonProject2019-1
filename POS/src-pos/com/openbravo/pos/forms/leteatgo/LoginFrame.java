    public void writeRestInfo() {
        FileWriter fw = null;
        try {
            File file = new File("restInfo");
            fw = new FileWriter(file);
            fw.write(owner.getR_no());
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fw.close();
            } catch (IOException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }
    
